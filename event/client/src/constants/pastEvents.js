// Helper function to get past events by year
export const getPastEventsByYear = (year) => {
  return PAST_EVENTS.filter(event => {
    const eventYear = new Date(event.date).getFullYear();
    return eventYear === year;
  });
};

// Helper function to get all unique years from past events
export const getPastEventYears = () => {
  const years = PAST_EVENTS.map(event => new Date(event.date).getFullYear());
  return [...new Set(years)].sort((a, b) => b - a); // Sort in descending order
};

// Helper function to get events by type
export const getEventsByType = (type) => {
  return PAST_EVENTS.filter(event => {
    if (type === 'paid') return event.payment.status === 'paid';
    if (type === 'free') return event.payment.status === 'free';
    if (type === 'team') return event.team === 'team';
    if (type === 'solo') return event.team === 'solo';
    return true;
  });
};
